package Class3;

public class E11ElseIfQuiz {
    public static void main(String[] args) {
        int age=20;
        if(age<1){
            System.out.println("Baby");

        }else if(age<20){
            System.out.println("teenager");
        }else if(age<30){
            System.out.println("still young");
        }else if(age<50){
            System.out.println("a little young");
        }else{
            System.out.println("old");
        }
    }
}
